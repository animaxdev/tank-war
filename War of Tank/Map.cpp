Map::Map()
{
	memcpy_s(m_map00, MAP_WIDTH * MAP_HIGH, Map::G_MAP, MAP_WIDTH * MAP_HIGH);
	//初始化使用默认地图
	memcpy_s(m_map01, MAP_WIDTH * MAP_HIGH, Map::G_MAP, MAP_WIDTH * MAP_HIGH);
}
Map::Map(const MAP map01 )
{
	memcpy_s(m_map00, MAP_WIDTH * MAP_HIGH, Map::G_MAP, MAP_WIDTH * MAP_HIGH);
	//根据传来的地图初始化
	memcpy_s(m_map01, MAP_WIDTH * MAP_HIGH, map01, MAP_WIDTH * MAP_HIGH);
}


Map::Map(const Map& map)
{
	memcpy_s(m_map00, MAP_WIDTH * MAP_HIGH, Map::G_MAP, MAP_WIDTH * MAP_HIGH);
	//初始化使用默认地图
	memcpy_s(m_map01, MAP_WIDTH * MAP_HIGH, map.m_map01, MAP_WIDTH * MAP_HIGH);
}

Map& Map::operator=(const Map& map)
{
	memcpy_s(m_map01, MAP_WIDTH * MAP_HIGH, map.m_map01, MAP_WIDTH * MAP_HIGH);
	return *this;
}
byte *Map::operator[](int index)
{
	return m_map01[index];
}

void Map::drawMap(void)
{
	for (int y = 0; y < MAP_HIGH; y++)
	{
		for (int x = 0; x < MAP_WIDTH; x++)
		{
			writeChar(y, x, wall_pic[m_map01[y][x]], wall_color[m_map01[y][x]]);
		}
	}
} //画出最初的地图

void Map::testdrawMap(void)
{
	for (int y = 0; y < MAP_HIGH; y++)
	{
		for (int x = 0; x < MAP_WIDTH; x++)
		{
			writeChar(y, x, wall_pic[m_map00[y][x]], F_CYAN);
		}
	}
} //画出最初的地图

void Map::putRes(int row, int col, byte id) throw(exception)
{
	//if (m_map01[row][col] > WALL_A && m_map01[row][col] != WALL_E)
		//throw exception("该位置上不能有其他物品");
		m_map00[row][col] = id;
}
void Map::movRes(int row, int col)
{
	m_map00[row][col] = 0;
}
bool Map:: isPlaced(int x, int y)
{
	if (m_map00[y][x] != 0)
	{
		return true;
	}
	return false;
} //是否已有东西在这个坐标上

unsigned int Map::getRecID(int x, int y)
{
	return m_map00[y][x];
}
